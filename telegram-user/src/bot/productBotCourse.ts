export function courseKeyboard(stepIndex: number): InlineKeyboard {
  const last = stepIndex >= PRODUCT_COURSE_STEPS.length - 1;
  const kb = new InlineKeyboard();
  if (!last) {
    kb.text("Далее", "crs:next");
  } else {
    kb.text("Я всё понял", "crs:done").row().text("Объяснить заново", "crs:replay");
  }
  return kb;
}
